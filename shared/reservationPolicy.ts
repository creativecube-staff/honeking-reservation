/**
 * 今日から何日先まで予約を受け付けるか(上限)。
 *
 * Why: 半年(180日)を上限にしておくと、
 * - 施術者の異動・退職・採用反映、営業時間変更、料金改定、メニュー改廃 等が予約に反映されないまま長期で抱えるリスクを抑えられる
 * - 「来年の予約まで取れちゃう」という UX 上の混乱を防げる
 *
 * 適用箇所:
 * - クライアント側(DateTimeStep): 「次の週 →」ボタンの無効化判定
 * - サーバ側(api/availability, api/reservations): 直接 URL や API を叩かれた場合のガード
 */
export const MAX_ADVANCE_DAYS = 180
